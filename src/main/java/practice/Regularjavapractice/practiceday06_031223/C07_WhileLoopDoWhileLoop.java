package practice.Regularjavapractice.practiceday06_031223;

public class C07_WhileLoopDoWhileLoop {
    public static void main(String[] args) {

        //Ne zaman While Loop? Ne zaman Do while Loop?

        //0 dan 3e kadar birer birer artan kodu yaziniz.

        int i = 0;

        while (i < 3) {
            System.out.println(i + " ");
            i++;
        }


        int b =0;
        do {

            System.out.println(b + " ");
            b++;

        } while (b < 3);


    }//Main
}//Class
