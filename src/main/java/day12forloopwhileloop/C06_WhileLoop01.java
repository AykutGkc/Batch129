package day12forloopwhileloop;

public class C06_WhileLoop01 {
    public static void main(String[] args) {

        //EX1: 3den 10a kadar tam sayilari konsola yazdirin.

        //1.Yol: For loop

        for (int i = 3; i <=10 ; i++) {
            System.out.print(i+ " ");
        }

        System.out.println();

        //2.Yol: While loop

        int i=3;
        while(i<11){

            System.out.print(i + " ");

            i++;
        }







    }//Main body
}//Class Body
